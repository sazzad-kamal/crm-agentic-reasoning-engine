"""LLM judge for SQL semantic equivalence."""

from __future__ import annotations

import logging

from pydantic import BaseModel, Field

from backend.core.llm import create_openai_chain

logger = logging.getLogger(__name__)


class JudgeResult(BaseModel):
    """Structured output from the SQL judge."""

    passed: bool = Field(description="Whether the generated SQL is semantically equivalent to expected SQL")
    errors: list[str] = Field(default_factory=list, description="List of issues found (required if passed=false)")


_SYSTEM_PROMPT = """Compare two SQL queries for semantic equivalence (same rows, same values).

Check:
- Tables and joins
- Columns selected
- Filters and conditions
- Aggregations and grouping
- Ordering and limits

Ignore:
- Aliases, column order, condition order
- Syntax style (JOIN vs comma-join, = vs IN)

If not equivalent, list specific differences."""

_HUMAN_PROMPT = """## Generated SQL
{generated_sql}

## Expected SQL
{expected_sql}"""


def judge_sql_equivalence(
    generated_sql: str,
    expected_sql: str,
) -> tuple[bool, list[str]]:
    """
    Use LLM to judge if generated SQL is semantically equivalent to expected SQL.

    Args:
        generated_sql: The SQL generated by the planner
        expected_sql: The expected/reference SQL

    Returns:
        Tuple of (passed: bool, errors: list[str])
        - passed: True if SQLs are semantically equivalent
        - errors: List of differences found (empty if passed)
    """
    chain = create_openai_chain(
        system_prompt=_SYSTEM_PROMPT,
        human_prompt=_HUMAN_PROMPT,
        max_tokens=512,
        structured_output=JudgeResult,
        streaming=False,
    )

    try:
        result: JudgeResult = chain.invoke({
            "generated_sql": generated_sql or "No SQL generated",
            "expected_sql": expected_sql,
        })

        logger.debug(f"SQL Judge: passed={result.passed}, errors={result.errors}")
        return result.passed, result.errors

    except Exception as e:
        logger.warning(f"SQL Judge error: {e}")
        return False, [f"Judge API error: {e}"]


__all__ = ["judge_sql_equivalence", "JudgeResult"]
