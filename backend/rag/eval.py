"""
RAG Evaluation Harness for Acme CRM docs.

Evaluates the RAG pipeline using:
- Context relevance: Did we retrieve the right documents?
- Answer relevance: Does the answer address the question?
- Groundedness: Is the answer grounded in the context (no hallucinations)?

Uses LLM-as-judge for relevance and groundedness scoring.

Usage:
    python -m backend.rag.eval
"""

import json
import time
from pathlib import Path
from typing import Optional
from pydantic import BaseModel

from backend.rag.retrieval import create_backend
from backend.rag.pipeline import answer_question
from backend.common.llm_client import call_llm


# =============================================================================
# Configuration
# =============================================================================

EVAL_QUESTIONS_PATH = Path("backend/rag/eval_questions.json")


# =============================================================================
# Judge Models
# =============================================================================

class JudgeResult(BaseModel):
    """Result from LLM judge evaluation."""
    context_relevance: int  # 0 or 1
    answer_relevance: int   # 0 or 1
    groundedness: int       # 0 or 1
    needs_human_review: int # 0 or 1
    confidence: float       # 0.0 to 1.0
    explanation: str


class EvalResult(BaseModel):
    """Complete evaluation result for a single question."""
    question_id: str
    question: str
    target_doc_ids: list[str]
    retrieved_doc_ids: list[str]
    answer: str
    judge_result: JudgeResult
    doc_recall: float  # What fraction of target docs were retrieved
    latency_ms: float
    total_tokens: int


# =============================================================================
# Judge Prompts
# =============================================================================

JUDGE_SYSTEM = """You are an expert evaluator for a RAG (Retrieval-Augmented Generation) system.
You will evaluate an answer generated by an AI assistant based on retrieved context.

Evaluate the following criteria (score 0 or 1):

1. CONTEXT_RELEVANCE: Are the retrieved documents relevant to the question?
   - 1 if the context contains information needed to answer the question
   - 0 if the context is completely irrelevant

2. ANSWER_RELEVANCE: Does the answer address the question?
   - 1 if the answer directly addresses what was asked
   - 0 if the answer is off-topic or doesn't address the question

3. GROUNDEDNESS: Is the answer grounded in the provided context?
   - 1 if all claims in the answer can be verified from the context
   - 0 if the answer contains hallucinations or unsupported claims

4. NEEDS_HUMAN_REVIEW: Should a human review this answer?
   - 1 if the answer is uncertain, potentially wrong, or sensitive
   - 0 if the answer appears reliable

Also provide:
- CONFIDENCE: Your confidence in this evaluation (0.0 to 1.0)
- EXPLANATION: Brief explanation of your scores

Respond in JSON format:
{
  "context_relevance": 0 or 1,
  "answer_relevance": 0 or 1,
  "groundedness": 0 or 1,
  "needs_human_review": 0 or 1,
  "confidence": 0.0 to 1.0,
  "explanation": "brief explanation"
}"""

JUDGE_PROMPT_TEMPLATE = """Question: {question}

Retrieved Context (doc_ids: {doc_ids}):
{context}

Generated Answer:
{answer}

Evaluate this RAG response:"""


# =============================================================================
# Judge Function
# =============================================================================

def judge_response(
    question: str,
    context: str,
    answer: str,
    doc_ids: list[str],
) -> JudgeResult:
    """
    Use LLM to judge the quality of a RAG response.
    
    Args:
        question: The original question
        context: The retrieved context
        answer: The generated answer
        doc_ids: List of doc_ids in the context
        
    Returns:
        JudgeResult with scores and explanation
    """
    prompt = JUDGE_PROMPT_TEMPLATE.format(
        question=question,
        doc_ids=", ".join(doc_ids),
        context=context[:2000],  # Truncate for judge
        answer=answer,
    )
    
    try:
        response = call_llm(
            prompt=prompt,
            system_prompt=JUDGE_SYSTEM,
            model="gpt-4.1-mini",
            max_tokens=500,
        )
        
        # Parse JSON response
        # Try to find JSON in the response
        import re
        json_match = re.search(r'\{[^{}]*\}', response, re.DOTALL)
        if json_match:
            result_dict = json.loads(json_match.group())
        else:
            result_dict = json.loads(response)
        
        return JudgeResult(
            context_relevance=int(result_dict.get("context_relevance", 0)),
            answer_relevance=int(result_dict.get("answer_relevance", 0)),
            groundedness=int(result_dict.get("groundedness", 0)),
            needs_human_review=int(result_dict.get("needs_human_review", 1)),
            confidence=float(result_dict.get("confidence", 0.5)),
            explanation=str(result_dict.get("explanation", "No explanation")),
        )
    
    except Exception as e:
        print(f"Warning: Judge failed: {e}")
        return JudgeResult(
            context_relevance=0,
            answer_relevance=0,
            groundedness=0,
            needs_human_review=1,
            confidence=0.0,
            explanation=f"Judge error: {str(e)}",
        )


# =============================================================================
# Evaluation Functions
# =============================================================================

def load_eval_questions(path: Path = EVAL_QUESTIONS_PATH) -> list[dict]:
    """Load evaluation questions from JSON file."""
    with open(path) as f:
        return json.load(f)


def compute_doc_recall(target_doc_ids: list[str], retrieved_doc_ids: list[str]) -> float:
    """Compute recall of target documents in retrieved documents."""
    if not target_doc_ids:
        return 1.0
    
    target_set = set(target_doc_ids)
    retrieved_set = set(retrieved_doc_ids)
    
    hits = len(target_set & retrieved_set)
    return hits / len(target_set)


def evaluate_question(
    question_data: dict,
    backend,
    verbose: bool = False,
) -> EvalResult:
    """
    Evaluate a single question through the RAG pipeline.
    
    Args:
        question_data: Dict with id, question, target_doc_ids
        backend: Initialized RetrievalBackend
        verbose: Print progress
        
    Returns:
        EvalResult with all metrics
    """
    question_id = question_data["id"]
    question = question_data["question"]
    target_doc_ids = question_data["target_doc_ids"]
    
    if verbose:
        print(f"\nEvaluating: {question_id}")
        print(f"  Question: {question[:60]}...")
    
    # Run RAG pipeline
    start_time = time.time()
    result = answer_question(question, backend, k=8, verbose=False)
    total_latency = (time.time() - start_time) * 1000
    
    # Build context string for judge
    context = "\n\n".join([
        f"[{c.doc_id}] {c.text[:500]}"
        for c in result["used_chunks"]
    ])
    
    # Compute doc recall
    doc_recall = compute_doc_recall(target_doc_ids, result["doc_ids_used"])
    
    if verbose:
        print(f"  Retrieved: {result['doc_ids_used']}")
        print(f"  Doc recall: {doc_recall:.2f}")
    
    # Judge the response
    judge_result = judge_response(
        question=question,
        context=context,
        answer=result["answer"],
        doc_ids=result["doc_ids_used"],
    )
    
    if verbose:
        print(f"  Judge: context={judge_result.context_relevance}, "
              f"answer={judge_result.answer_relevance}, "
              f"grounded={judge_result.groundedness}")
    
    return EvalResult(
        question_id=question_id,
        question=question,
        target_doc_ids=target_doc_ids,
        retrieved_doc_ids=result["doc_ids_used"],
        answer=result["answer"],
        judge_result=judge_result,
        doc_recall=doc_recall,
        latency_ms=total_latency,
        total_tokens=result["metrics"]["total_tokens"],
    )


def run_evaluation(
    questions: Optional[list[dict]] = None,
    verbose: bool = True,
) -> list[EvalResult]:
    """
    Run full evaluation over all questions.
    
    Args:
        questions: List of question dicts (or load from file)
        verbose: Print progress
        
    Returns:
        List of EvalResult objects
    """
    if questions is None:
        questions = load_eval_questions()
    
    print("=" * 60)
    print("RAG Evaluation Harness")
    print("=" * 60)
    print(f"Evaluating {len(questions)} questions\n")
    
    # Initialize backend
    backend = create_backend()
    
    results = []
    for q in questions:
        result = evaluate_question(q, backend, verbose=verbose)
        results.append(result)
    
    return results


def print_summary(results: list[EvalResult]) -> None:
    """Print summary statistics from evaluation results."""
    n = len(results)
    
    if n == 0:
        print("No results to summarize")
        return
    
    # Compute aggregates
    context_relevance = sum(r.judge_result.context_relevance for r in results) / n
    answer_relevance = sum(r.judge_result.answer_relevance for r in results) / n
    groundedness = sum(r.judge_result.groundedness for r in results) / n
    needs_review = sum(r.judge_result.needs_human_review for r in results) / n
    
    # RAG triad success (all three = 1)
    triad_success = sum(
        1 for r in results
        if r.judge_result.context_relevance == 1
        and r.judge_result.answer_relevance == 1
        and r.judge_result.groundedness == 1
    ) / n
    
    avg_doc_recall = sum(r.doc_recall for r in results) / n
    avg_latency = sum(r.latency_ms for r in results) / n
    total_tokens = sum(r.total_tokens for r in results)
    
    # Approximate cost (GPT-4.1-mini pricing)
    # Input: $0.40 per 1M tokens, Output: $1.60 per 1M tokens
    # Rough estimate assuming 80% input, 20% output
    estimated_cost = (total_tokens * 0.8 * 0.40 + total_tokens * 0.2 * 1.60) / 1_000_000
    
    print("\n" + "=" * 60)
    print("EVALUATION SUMMARY")
    print("=" * 60)
    
    print(f"\n{'Metric':<25} {'Value':>10}")
    print("-" * 40)
    print(f"{'Questions evaluated':<25} {n:>10}")
    print(f"{'Context Relevance':<25} {context_relevance:>10.1%}")
    print(f"{'Answer Relevance':<25} {answer_relevance:>10.1%}")
    print(f"{'Groundedness':<25} {groundedness:>10.1%}")
    print(f"{'RAG Triad Success':<25} {triad_success:>10.1%}")
    print(f"{'Needs Human Review':<25} {needs_review:>10.1%}")
    print(f"{'Avg Doc Recall':<25} {avg_doc_recall:>10.1%}")
    print("-" * 40)
    print(f"{'Avg Latency (ms)':<25} {avg_latency:>10.0f}")
    print(f"{'Total Tokens':<25} {total_tokens:>10,}")
    print(f"{'Est. Cost ($)':<25} {estimated_cost:>10.4f}")
    
    # Per-question details
    print("\n" + "-" * 60)
    print("PER-QUESTION RESULTS")
    print("-" * 60)
    print(f"{'ID':<6} {'Ctx':<4} {'Ans':<4} {'Gnd':<4} {'Recall':<8} {'Latency':<10}")
    print("-" * 60)
    
    for r in results:
        print(f"{r.question_id:<6} "
              f"{r.judge_result.context_relevance:<4} "
              f"{r.judge_result.answer_relevance:<4} "
              f"{r.judge_result.groundedness:<4} "
              f"{r.doc_recall:<8.1%} "
              f"{r.latency_ms:<10.0f}ms")
    
    # Failed questions
    failed = [r for r in results if r.judge_result.groundedness == 0 or r.judge_result.answer_relevance == 0]
    if failed:
        print("\n" + "-" * 60)
        print("QUESTIONS NEEDING ATTENTION")
        print("-" * 60)
        for r in failed:
            print(f"\n{r.question_id}: {r.question}")
            print(f"  Judge: {r.judge_result.explanation}")


# =============================================================================
# CLI Entrypoint
# =============================================================================

def main():
    """Main entrypoint for evaluation."""
    results = run_evaluation(verbose=True)
    print_summary(results)
    
    # Save results to file
    output_path = Path("data/processed/eval_results.json")
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    results_data = [r.model_dump() for r in results]
    with open(output_path, "w") as f:
        json.dump(results_data, f, indent=2)
    
    print(f"\nResults saved to {output_path}")


if __name__ == "__main__":
    main()
